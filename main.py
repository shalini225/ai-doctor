@app.route("/predict", methods=["POST"])
def predict():
    if "user" not in session:
        return redirect("/login")

    name = request.form.get("name")
    age = request.form.get("age")
    selected_language = session.get("language", "en")

    # Create input vector
    input_data = [0] * len(columns)
    selected_symptoms = []

    for symptom in columns:
        if request.form.get(symptom) == "on":
            index = list(columns).index(symptom)
            input_data[index] = 1
            selected_symptoms.append(symptom)

    final_input = [input_data]

    # üîπ Predict disease
    prediction_encoded = model.predict(final_input)[0]
    prediction = le.inverse_transform([prediction_encoded])[0]

    # üîπ Confidence (optional simple)
    if hasattr(model, "predict_proba"):
        probabilities = model.predict_proba(final_input)
        confidence = round(max(probabilities[0]) * 100, 2)
    else:
        confidence = 75.0

    # üîπ Save to CSV (NORMAL)
    with open("history.csv", "a", newline="", encoding="utf-8") as file:
        writer = csv.writer(file)
        writer.writerow([
            session["user"],
            name,
            age,
            datetime.datetime.now().strftime("%Y-%m-%d %H:%M:%S"),
            ", ".join(selected_symptoms),
            prediction
        ])

    # üîπ Translate disease
    translated_disease = disease_info.get(
        prediction, {}
    ).get("name", {}).get(selected_language, prediction)

    return render_template(
        "result.html",
        disease=translated_disease,
        confidence=confidence
    )






